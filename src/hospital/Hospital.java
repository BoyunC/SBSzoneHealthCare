package hospital;

public class Hospital {
	private String name;
	
	// Constructor
	public Hospital() {}

	public Hospital(String name) {
		super();
		this.name = name;
	}

	// Getter Setter
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
